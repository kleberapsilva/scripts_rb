#!/usr/bin/env zsh
(
#---------------------------------------------------------------------------------------------------------------------
USUARIO_LOGADO=$( who | egrep console | awk '{print $1}' | egrep "(^[a-zA-Z]{1}[0-9]{6}$)|(^[a-zA-Z]*\.[a-zA-Z]{1}[0-9]{3,4}$)" )
#---------------------------------------------------------------------------------------------------------------------
su - $USUARIO_LOGADO << "EOF"
PATH="$PATH:/Users/$(whoami)/.brew/bin"
#---------------------------------------------------------------------------------------------------------------------
echo "\n🛠️. Desinstalando ASDF!\n"
brew uninstall asdf
rm -rf /Users/$(whoami)/.asdf
sed -i -- 's/\. $(brew --prefix asdf)\/libexec\/asdf.sh//g' /Users/$(whoami)/.zshrc
sed -i -- 's/\. $(brew --prefix asdf)\/libexec\/asdf.sh//g' /Users/$(whoami)/.bashrc
sed -i -- 's/\. $(brew --prefix asdf)\/etc\/bash_completion.d\/asdf.bash//g' /Users/$(whoami)/.bashrc
#---------------------------------------------------------------------------------------------------------------------
echo "\n🛠️. Desinstalando JQ!\n"
brew uninstall jq
#---------------------------------------------------------------------------------------------------------------------
echo "\n✅ Desinstalações concluidas! ✅"
#---------------------------------------------------------------------------------------------------------------------
EOF
#---------------------------------------------------------------------------------------------------------------------
) 2>&1 | tee -a /tmp/ASDF.log
